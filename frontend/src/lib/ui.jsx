export function Icon({ name, size = 16, strokeWidth = 2, className = '' }) {
  const cls = `inline-block ${className}`;
  const Brand = BRAND[name];
  if (Brand) return <Brand width={size} height={size} className={cls} />;
  const Cmp = icons[name] ?? icons.Circle;
  return <Cmp size={size} strokeWidth={strokeWidth} className={cls} />;
}

export function AnimCounter({ to, durationMs = 900, format = (v) => v.toLocaleString('fr-FR'), className = '' }) {
  const [val, setVal] = useState(0);
  useEffect(() => {
    let start = null, raf;
    const step = (ts) => {
      if (!start) start = ts;
      const p = Math.min(1, (ts - start) / durationMs);
      const eased = 1 - Math.pow(1 - p, 3);
      setVal(Math.round(to * eased));
      if (p < 1) raf = requestAnimationFrame(step);
    };
    raf = requestAnimationFrame(step);
    return () => cancelAnimationFrame(raf);
  }, [to, durationMs]);
  return <span className={className}>{format(val)}</span>;
}

export function formatScore(n) {
  if (n == null) return '';
  return n >= 1000 ? `${(n / 1000).toFixed(1)}k` : `${n}`;
}

export function timeAgo(iso) {
  if (!iso) return '—';
  const s = Math.floor((Date.now() - new Date(iso).getTime()) / 1000);
  if (s < 60) return `il y a ${s}s`;
  if (s < 3600) return `il y a ${Math.floor(s / 60)}min`;
  if (s < 86400) return `il y a ${Math.floor(s / 3600)}h`;
  return `il y a ${Math.floor(s / 86400)}j`;
}
